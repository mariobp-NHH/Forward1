function togglePersonInfo1() {
  // Hent knappen ved hjelp av ID-attributtet
  var toggleButton = document.getElementById("toggle-button-1");
  // Sett opp en klikk-hendelse for knappen
  toggleButton.addEventListener("click", function() {

    // Hent teksten som skal skjules/avsløres
    var text = document.getElementById("person-info-1");

    // Skjul/avslør teksten ved å endre CSS display-attributten
    if (text.style.display === "none") {
      text.style.display = "block";
      toggleButton.innerHTML = '<span id="toggle-text">Read less</span>  <i class="fas fa-caret-up"></i>';
    } else {
      text.style.display = "none";
      toggleButton.innerHTML = '<span id="toggle-text">Read more</span>  <i class="fas fa-caret-down"></i>';
    }
  });
  }
